char	*ft_strcapitalize(char *str)
{
	int c;

	c=0;
	while (str[c] != '\0')
	{
		if(str[0] >= 97 && str[0] <=122)
		{
			str[0]=str[0] - 32;
		}	
	
		if((str[c-1] < 48 || str[c-1] >57 && str[c-1] < 65 || str[c-1] >90 && str[c-1]< 97|| str[c-1]>122 )&& (str[c] >= 97 && str[c] <= 122))
		{
			str[c] = str[c] -32;
		}
	
		if(((str[c-1] >= 48 && str[c-1] <= 57) || (str[c-1] >= 65 && str[c-1] <= 90 ) || (str[c-1] >= 97 && str[c-1] <= 122 ) ) && (str[c] >= 65 && str[c] <=90))
		{
			str[c]=str[c] + 32;
		}	
	
	c++;
	}
	return(str);	
}

	

